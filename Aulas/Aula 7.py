n1 = int(input('Um valor: '))
n2 = int(input('Mais um valor: '))
a = n1 + n2
s = n1 - n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2
dr = n1 % n2
print('A soma é {} , o subtração é {} , o produto é {}!'.format(a, s, m))
print('A divisão é {} , a divisão inteira é {} , a potência é {}'.format(d, di, e))
print('O resto da divisão é {} .'.format(dr))
